package com.example.asus.bhagalpur;

public class LocationModelClass {
    private String name;
    private int image;

    public LocationModelClass(String name, int image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }
}
